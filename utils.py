def add(a: int, b: int) -> int:
    '''Sum'''
    return a + b


def subtract(a: int, b: int) -> int:
    '''difference'''
    return a - b


def multiply(a: int, b: int) -> int:
    '''product'''
    return a * b


def divide(a: int, b: int) -> float:
    '''quotient'''
    return a / b
